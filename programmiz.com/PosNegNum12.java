//check whether a given number is positive or negative. This is done by using a if else statement in Java.

public class PosNegNum12 {
    public static void main (String[] args)
    {
        int a = -5;

        if (a >= 0 )
        {
            System.out.println(a + " is a positive number");
        }
        else
        {
            System.out.println(a + " is a negative number");
        }
    }
}


