/*
    store and add two integer numbers in Java.
    After addition, the final sum is displayed on the screen.

*/

public class AllExamples2 {
    class Main{
        public static void main (String[] args)
        {
            System.out.println("Enter two numbers");
            int first = 10;
            int second = 20;

            System.out.println(first + " " + second);

            //add two numbers
            int sum = first + second;
            System.out.println("The sum is:" + sum);
        }
    }
}
